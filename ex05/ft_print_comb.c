#include <unistd.h>

void	ft_print_comb(void)
{
	char	T[3];

	T[0] = '0';
	T[1] = '1';
	T[2] = '2';
	while (T[0] <= '7')
	{
		T[1] = T[0] + 1;
		while (T[1] <= '8')
		{
			T[2] = T[1] + 1;
			while (T[2] <= '9')
			{
				write(1, &T[0], 1);
				write(1, &T[1], 1);
				write(1, &T[2], 1);
				if (T[0] != '7' || T[1] != '8' || T[2] != '9')
					write(1, ", ", 2);
				T[2]++;
			}
			T[1]++;
		}
		T[0]++;
	}
	write(1, "\n", 1);
}
