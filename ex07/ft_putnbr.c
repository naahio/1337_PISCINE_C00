#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;
	if ( nb < 0)
		{
			nb = -nb;
			write(1, "-", 1);
		}
		
	if (nb % 10 != 0)
	{
		c = nb % 10 + '0';
		nb = nb / 10;
		ft_putnbr(nb);
		write(1, &c, 1);
	}
	
}
 int main()
 {
	 ft_putnbr(102);
 }