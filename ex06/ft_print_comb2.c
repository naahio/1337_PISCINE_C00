#include <unistd.h>

void	comb(char	a, char	b, char	c, char	d)
{
	write(1, &a, 1);
	write(1, &b, 1);
	write(1, " ", 1);
	write(1, &c, 1);
	write(1, &d, 1);
	if (a != '9' || b != '8' || c != '9' || d != '9')
		write(1, ", ", 2);
}

void	ft_print_comb2(void)
{
	char	T[4];

	T[0] = '0';
	while (T[0] <= '9')
	{
		T[1] = '0';
		while (T[1] <= '8')
		{
			T[2] = T[0];
			while (T[2] <= '9')
			{
				T[3] = T[1] + 1;
				while (T[3] <= '9')
				{
					comb(T[0], T[1], T[2], T[3]);
					T[3]++;
				}
				T[2]++;
			}
			T[1]++;
		}
		T[0]++;
	}
}
